package DataTypes;

public class PrimitiveData {
    public static void main(String[]args){

       // byte
        byte b = 100;
        System.out.println("Byte value : " +b);

        // shorts
        short s = 123;
        System.out.println("Short values : " +s);

        //int
        int i = 12345;
        System.out.println("Int value : " +i);

        int calc = -98765;
        System.out.println("int second value : " +calc);

        //Long
        long amountval = 123456789L;
        System.out.println(" Long value : " +amountval);

        //float
        float intrestRate = 12.50f;
        System.out.println("Float value : " +intrestRate);

        //double
        double ob = 12345.234;
        System.out.println("double value : " +ob);

        //boolean
        boolean val = true;
        System.out.println("boolean value : " +val);

        boolean val1 = false;
        System.out.println("boolean value : " +val1);

        //char
        char ch = 'a';
        System.out.println("char value : " +ch);

        char ch1 = 10; //code for x
        System.out.println("Char value : " +ch1);


    }
}
